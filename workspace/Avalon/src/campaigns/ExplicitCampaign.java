package campaigns;


public class ExplicitCampaign {
	int startRound;
	int endRound;
	public int getStartRound() {
		return startRound;
	}
	public void setStartRound(int startRound) {
		this.startRound = startRound;
	}
	public int getEndRound() {
		return endRound;
	}
	public void setEndRound(int endRound) {
		this.endRound = endRound;
	}
	
}
